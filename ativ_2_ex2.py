
# Receber a temperatura em °C:

celsius = float(input("Informe a temperatura em ºC: "))

# Convertendo para °Fahrenheit

# f = (c * 1.8) + 32

celsius_to_fahrenheit = celsius * 1.8 + 32
print(f"{celsius} °C equivalem a {celsius_to_fahrenheit} °F")

# Convertendo para Kelvin

# k = c + 273,15

celsius_to_kelvin = celsius + 273.15
print(f"{celsius} °C equivalem a {celsius_to_kelvin} K")


